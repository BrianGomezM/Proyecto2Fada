
package proyecto2fada.Clases;


public class Varilla {
    int longitud, valor;

    public Varilla(int longitud, int valor) {
        this.longitud = longitud;
        this.valor = valor;
    }
    
     public Varilla() {
        
    }
    public int getLongitud() {
        return longitud;
    }

    public void setLongitud(int longitud) {
        this.longitud = longitud;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }
    
    
    
    
}
